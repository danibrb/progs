// calcolo area rettangolo dati base e altezza
#include <iostream>
using namespace std;
main()
{
    double base, altezza, area;
    cout << "dammi base e altezza del rettangolo " << endl;
    cin >> base >> altezza;
    area = base * altezza;
    cout << "l'area del rettangolo vale: " << area << endl;
}